def match_emojis(prefix: str, limit: int = 5) -> list[tuple[str, str]]:
    """Return up to `limit` (shortcode, glyph) pairs whose name starts with prefix."""
    if not prefix:
        return []
    prefix = prefix.lower()
    exact = [(k, v) for k, v in EMOJI.items() if k == prefix]
    prefixed = [(k, v) for k, v in EMOJI.items() if k.startswith(prefix) and k != prefix]
    prefixed.sort(key=lambda kv: (len(kv[0]), kv[0]))
    return (exact + prefixed)[:limit]
